function visualProfile(mode) {
  if (mode === 'dark') {
    return {
      blur: 45,
      opacity: 2,
      saturation: 1,
      contrast: 1,
      brightness: 1,
    }
  }
  return {
    blur: 45,
    opacity: 2,
    saturation: 1,
    contrast: 1,
    brightness: 1,
  }
}

export default visualProfile
